- (void)insertNewObject:(id)sender
{
    if (!_library.shelves) {
        NSLog(@"library.shelves reset");
        _library.shelves = [[NSMutableArray alloc] init];
    }
    [_library.shelves insertObject:[[CFLShelf alloc] initWithTitle:@"New Shelf"] atIndex:0];
    NSIndexPath *indexPath = [NSIndexPath indexPathForRow:0 inSection:0];
    [self.tableView insertRowsAtIndexPaths:@[indexPath] withRowAnimation:UITableViewRowAnimationAutomatic];
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return _library.shelves.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"MyBasicCell" forIndexPath:indexPath];
    
    CFLShelf *shelf = [_library.shelves objectAtIndex:indexPath.row];
    cell.textLabel.text = shelf.title;
    
    return cell;
}


